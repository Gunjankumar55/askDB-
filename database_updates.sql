USE sql_chatbot;

-- Create tables
CREATE TABLE customers (
    customer_id INT AUTO_INCREMENT,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    email VARCHAR(100) UNIQUE,
    phone VARCHAR(20),
    registration_date DATE,
    last_activity TIMESTAMP,
    PRIMARY KEY (customer_id)
);

CREATE TABLE customer_addresses (
    address_id INT PRIMARY KEY AUTO_INCREMENT,
    customer_id INT,
    address_type ENUM('Home', 'Work', 'Billing', 'Other') DEFAULT 'Home',
    street_address VARCHAR(255),
    city VARCHAR(100),
    state VARCHAR(100),
    postal_code VARCHAR(20),
    country VARCHAR(100),
    FOREIGN KEY (customer_id) REFERENCES customers(customer_id)
);

CREATE TABLE credit_scores (
    score_id INT PRIMARY KEY AUTO_INCREMENT,
    customer_id INT UNIQUE,
    credit_score INT,
    score_status ENUM('Low', 'Medium', 'High') NOT NULL,
    last_updated TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (customer_id) REFERENCES customers(customer_id),
    INDEX (score_status),
    INDEX (last_updated)
);

CREATE TABLE customer_transactions (
    transaction_id INT PRIMARY KEY AUTO_INCREMENT,
    customer_id INT,
    transaction_date DATE NOT NULL,
    amount DECIMAL(12,2) NOT NULL,
    transaction_type ENUM('Credit', 'Debit') NOT NULL,
    description VARCHAR(255),
    category ENUM('Salary', 'Investment', 'Purchase', 'Utility', 'Transfer', 'Other'),
    FOREIGN KEY (customer_id) REFERENCES customers(customer_id),
    INDEX (transaction_date),
    INDEX (category)
);

CREATE TABLE customer_loans (
    loan_id INT PRIMARY KEY AUTO_INCREMENT,
    customer_id INT,
    principal_amount DECIMAL(12,2) NOT NULL,
    interest_rate DECIMAL(5,2) NOT NULL,
    start_date DATE NOT NULL,
    duration_months INT NOT NULL,
    monthly_payment DECIMAL(10,2) NOT NULL,
    end_date DATE AS (DATE_ADD(start_date, INTERVAL duration_months MONTH)) STORED,
    loan_status ENUM('Active', 'Paid Off', 'Defaulted', 'Delayed'),
    last_updated TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (customer_id) REFERENCES customers(customer_id),
    INDEX (loan_status),
    INDEX (end_date)
);

